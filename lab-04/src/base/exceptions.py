"""
Модуль exceptions.py
====================
Содержит иерархию кастомных исключений для системы учета сотрудников.

Использование собственных исключений позволяет четко разделять ошибки
бизнес-логики (например, нарушение целостности данных) от системных ошибок
(например, FileNotFoundError).
"""

class AppError(Exception):
    """Базовый класс для всех исключений уровня приложения."""
    pass

class EmployeeNotFoundError(AppError):
    """Возникает при попытке обращения к несуществующему сотруднику."""
    pass

class DepartmentNotFoundError(AppError):
    """Возникает при попытке обращения к несуществующему отделу."""
    pass

class ProjectNotFoundError(AppError):
    """Возникает при попытке обращения к несуществующему проекту."""
    pass

class InvalidStatusError(AppError):
    """
    Возникает при попытке установить статус проекта, который не входит
    в список разрешенных (planning, active, completed, cancelled).
    """
    pass

class DuplicateIdError(AppError):
    """
    Возникает при попытке создать сущность (сотрудника, проект, отдел)
    с идентификатором, который уже используется в системе.
    """
    pass

class DependencyError(AppError):
    """
    Критическая ошибка целостности данных.
    Возникает при попытке удалить объект, от которого зависят другие объекты
    (например, удаление отдела, в котором числятся сотрудники, или удаление
    сотрудника, назначенного на активный проект).
    """
    pass
