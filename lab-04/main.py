import sys
import os

# Добавляем корневую папку проекта в путь поиска Python.
# Это необходимо, чтобы импорты вида 'from base.employee import Employee'
# корректно работали при запуске main.py как основного скрипта.
# Без этого Python не будет "видеть" папки 'base', 'specialists', 'tests'.
sys.path.append(os.path.dirname(os.path.abspath(__file__)))

# Импортируем классы-тестеры из папки tests
try:
    from tests.test_part1 import TestPart1
    from tests.test_part2 import TestPart2
except ImportError as e:
    print("Ошибка импорта! Убедитесь, что структура проекта верна и во всех папках")
    print("('base', 'specialists', 'tests') есть пустой файл __init__.py")
    print(f"Детали ошибки: {e}")
    sys.exit(1)  # Завершаем выполнение, если базовые компоненты не найдены

if __name__ == "__main__":
    """
    Главная точка входа в программу.
    Запускает все тестовые сценарии для лабораторных работ.
    """
    print("==========================================")
    print("   СИСТЕМА УЧЕТА СОТРУДНИКОВ: TEST RUNNER")
    print("==========================================\n")

    # --- ЗАПУСК ТЕСТОВ ---
    # Каждая часть лабораторной работы тестируется отдельно.

    # Запускаем тесты для Части 1 (Инкапсуляция)
    TestPart1.run()

    # Запускаем тесты для Части 2 (Наследование и Фабрика)
    TestPart2.run()

    # Сюда в будущем можно будет добавить вызовы TestPart3.run() и т.д.

    print("==========================================")
    print("   Все тестовые сценарии выполнены.")
    print("==========================================")

